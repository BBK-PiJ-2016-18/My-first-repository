String s = args[1]
double n1 = Double.parseDouble(args[0])
double n2 = Double.parseDouble(args [2])
if (s == "+") {
	double ans = n1 + n2
	println n1 + " + " + n2 + " = " + ans}
if (s == "-") {
	double ans = n1 - n2
	println n1 + " - " + n2 + " = " + ans}
if (s == "x") {
	double ans = n1 * n2
	println n1 + " * " + n2 + " = " + ans}
if (s == "/") {
	double ans = n1 / n2
	println n1 + " / " + n2 + " = " + ans}